        // Get modal elements
        const modal = document.getElementById('chapterModal');
        const modalTitle = document.getElementById('modalSubjectTitle');
        const chapterList = document.getElementById('chapterList');
        const closeModalBtn = document.querySelector('.close-modal');

        // Function to open modal with chapters
        function openModal(subjectName) {
            const chapters = chaptersData[subjectName];
            if (chapters) {
                modalTitle.textContent = `${subjectName} - Chapters`;
                
                // Clear previous chapters
                chapterList.innerHTML = '';
                
                // Add chapters to list
                chapters.forEach((chapter, index) => {
                    const chapterDiv = document.createElement('div');
                    chapterDiv.className = 'chapter-item';
                    
                    const chapterNameSpan = document.createElement('span');
                    chapterNameSpan.className = 'chapter-name';
                    chapterNameSpan.textContent = chapter.name;
                    
                    const downloadLink = document.createElement('a');
                    downloadLink.href = chapter.link;
                    downloadLink.target = '_blank';
                    downloadLink.className = 'download-chapter';
                    downloadLink.textContent = '📥 Download';
                    
                    chapterDiv.appendChild(chapterNameSpan);
                    chapterDiv.appendChild(downloadLink);
                    chapterList.appendChild(chapterDiv);
                });
                
                modal.style.display = 'flex';
            }
        }

        // Close modal function
        function closeModal() {
            modal.style.display = 'none';
        }

        // Add click event to all notes cards
        const noteCards = document.querySelectorAll('.notes');
        noteCards.forEach(card => {
            card.addEventListener('click', function(e) {
                const subject = this.getAttribute('data-subject');
                
                // Create ripple effect
                const ripple = document.createElement('span');
                const rect = this.getBoundingClientRect();
                const size = Math.max(rect.width, rect.height);
                const x = e.clientX - rect.left - size / 2;
                const y = e.clientY - rect.top - size / 2;
                
                ripple.style.width = ripple.style.height = `${size}px`;
                ripple.style.left = `${x}px`;
                ripple.style.top = `${y}px`;
                ripple.classList.add('ripple-effect');
                this.appendChild(ripple);
                
                setTimeout(() => {
                    ripple.remove();
                }, 500);
                
                // Open modal with chapters
                setTimeout(() => {
                    openModal(subject);
                }, 100);
            });
        });

        // Close modal when clicking close button
        closeModalBtn.addEventListener('click', closeModal);
        
        // Close modal when clicking outside
        window.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeModal();
            }
        });
        
