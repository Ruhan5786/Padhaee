        const modal = document.getElementById('pyqModal');
        const modalTitle = document.getElementById('modalTitle');
        const modalBody = document.getElementById('modalBody');
        const closeModalBtn = document.querySelector('.close-modal');

        function openModal(card) {
            const yearName = card.getAttribute('data-year-name');
            const session = card.getAttribute('data-session');
            const sem1 = card.getAttribute('data-sem1');
            const sem2 = card.getAttribute('data-sem2');
            
            modalTitle.textContent = `${yearName} ${session} - PYQs`;
            modalBody.innerHTML = '';
            
            // Add Semester 1 if exists
            if (sem1) {
                const key = `${session}_${sem1}`;
                if (subjectsData[key]) {
                    const semesterDiv = document.createElement('div');
                    semesterDiv.className = 'semester-section';
                    semesterDiv.innerHTML = `
                        <div class="semester-title">📄 ${sem1}</div>
                        <div class="subjects-list" id="subjects-${sem1.replace(/\s/g, '')}"></div>
                    `;
                    modalBody.appendChild(semesterDiv);
                    
                    const subjectsList = semesterDiv.querySelector(`.subjects-list`);
                    subjectsData[key].forEach(subject => {
                        const subjectItem = document.createElement('div');
                        subjectItem.className = 'subject-item';
                        subjectItem.innerHTML = `
                            <span class="subject-name">${subject.name}</span>
                            <a href="${subject.link}" target="_blank" class="download-subject">📥 Download</a>
                        `;
                        subjectsList.appendChild(subjectItem);
                    });
                }
            }
            
            // Add Semester 2 if exists
            if (sem2) {
                const key = `${session}_${sem2}`;
                if (subjectsData[key]) {
                    const semesterDiv = document.createElement('div');
                    semesterDiv.className = 'semester-section';
                    semesterDiv.innerHTML = `
                        <div class="semester-title">📄 ${sem2}</div>
                        <div class="subjects-list" id="subjects-${sem2.replace(/\s/g, '')}"></div>
                    `;
                    modalBody.appendChild(semesterDiv);
                    
                    const subjectsList = semesterDiv.querySelector(`.subjects-list`);
                    subjectsData[key].forEach(subject => {
                        const subjectItem = document.createElement('div');
                        subjectItem.className = 'subject-item';
                        subjectItem.innerHTML = `
                            <span class="subject-name">${subject.name}</span>
                            <a href="${subject.link}" target="_blank" class="download-subject">📥 Download</a>
                        `;
                        subjectsList.appendChild(subjectItem);
                    });
                }
            }
            
            modal.style.display = 'flex';
        }

        function closeModal() {
            modal.style.display = 'none';
        }

        const downloadBtns = document.querySelectorAll('.download-btn');
        downloadBtns.forEach(btn => {
            btn.addEventListener('click', function(e) {
                e.stopPropagation();
                const card = this.closest('.pyq-card');
                openModal(card);
            });
        });

        closeModalBtn.addEventListener('click', closeModal);
        
        window.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeModal();
            }
        });

        const filterBtns = document.querySelectorAll('.filter-btn');
        const pyqCards = document.querySelectorAll('.pyq-card');

        filterBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                filterBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                const filterValue = btn.getAttribute('data-filter');
                pyqCards.forEach(card => {
                    if (filterValue === 'all' || card.getAttribute('data-year') === filterValue) {
                        card.style.display = 'block';
                    } else {
                        card.style.display = 'none';
                    }
                });
            });
        });

